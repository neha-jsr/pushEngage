const MILS_IN_SEC = 1000;
const MILS_IN_MIN = 60 * MILS_IN_SEC;
const MILS_IN_HOUR = 60 * MILS_IN_MIN;
const MILS_IN_DAY = 24 * MILS_IN_HOUR;

module.exports.MILS_IN_SEC = MILS_IN_SEC;
module.exports.MILS_IN_MIN = MILS_IN_MIN;
module.exports.MILS_IN_HOUR = MILS_IN_HOUR;
module.exports.MILS_IN_DAY = MILS_IN_DAY;